#!/usr/bin/env node
/*
 * Reads every public/widgets/*-/widget.json and emits a Markdown table for
 * the README. Run with `npm run build:widget-catalog` (writes to stdout) or
 * `node scripts/build-widget-catalog.js > docs/widgets.md`.
 *
 * Output format: a Markdown table with columns Widget | Description | Size |
 * Params | Cap-gated. Skips widgets that don't have a widget.json (those are
 * considered internal helpers).
 */
const fs = require('fs');
const path = require('path');

const ROOT = path.resolve(__dirname, '..');
const WIDGETS_DIR = path.join(ROOT, 'public', 'widgets');

function listWidgets() {
  return fs.readdirSync(WIDGETS_DIR, { withFileTypes: true })
    .filter(e => e.isDirectory())
    .map(e => e.name)
    .sort();
}

function readManifest(slug) {
  const p = path.join(WIDGETS_DIR, slug, 'widget.json');
  if (!fs.existsSync(p)) return null;
  try { return JSON.parse(fs.readFileSync(p, 'utf-8')); }
  catch (e) { console.error(`# warn: ${slug}/widget.json: ${e.message}`); return null; }
}

function formatParams(params) {
  if (!params || typeof params !== 'object') return '—';
  const parts = Object.entries(params).map(([k, v]) => {
    const def = v && v.default !== undefined ? `=${v.default}` : '';
    return `\`?${k}${def}\``;
  });
  return parts.join(', ');
}

function formatSize(size) {
  if (!size || !size.w || !size.h) return '—';
  return `${size.w}×${size.h}`;
}

function formatCap(requiresCap) {
  if (!requiresCap) return '—';
  // Common values: hasChamberTemp, hasDualNozzle, hasDualAMS
  return `\`${requiresCap}\``;
}

function main() {
  const slugs = listWidgets();
  const rows = [];
  for (const slug of slugs) {
    const m = readManifest(slug);
    if (!m) continue; // skip internal helpers without manifests
    rows.push({
      slug,
      name: m.name || slug,
      description: m.description || '',
      size: formatSize(m.recommendedSize),
      params: formatParams(m.params),
      cap: formatCap(m.requiresCap),
    });
  }

  // Print Markdown
  console.log('| Widget | Description | Recommended size | Params | Cap-gated |');
  console.log('|--------|-------------|------------------|--------|-----------|');
  for (const r of rows) {
    const safeDesc = r.description.replace(/\|/g, '\\|');
    console.log(`| **${r.name}** (\`${r.slug}\`) | ${safeDesc} | ${r.size} | ${r.params} | ${r.cap} |`);
  }
  console.log('');
  console.log(`_${rows.length} widgets — generated by \`scripts/build-widget-catalog.js\`._`);
}

main();
